class TestIFCParserAgent:
    """Tests for the IFC Parser Agent."""

    def test_parser_executes(self):
        agent = IFCParserAgent()
        state = {
            "ifc_file_path": str(SAMPLE_IFC),
            "project_config": {},
            "parsed_elements": [],
            "building_info": None,
            "classified_elements": {},
            "calculated_quantities": [],
            "material_list": [],
            "boq_data": None,
            "boq_file_paths": {},
            "validation_report": None,
            "warnings": [],
            "errors": [],
            "status": ProcessingStatus.PENDING,
            "current_step": "",
            "processing_log": [],
        }

        result = asyncio.run(agent.execute(state))

        # Building info should be populated
        assert result["building_info"] is not None
        assert result["building_info"]["project_name"] == "Sample House"
        assert len(result["building_info"]["storeys"]) == 2

        # All 56 elements should be parsed
        assert len(result["parsed_elements"]) == 56

        # Check that elements have the right structure
        first_elem = result["parsed_elements"][0]
        assert "ifc_id" in first_elem
        assert "ifc_type" in first_elem
        assert "quantities" in first_elem
        assert "materials" in first_elem

        # Processing log should have an entry
        assert len(result["processing_log"]) > 0

    def test_parser_handles_missing_file(self):
        agent = IFCParserAgent()
        state = {
            "ifc_file_path": "nonexistent.ifc",
            "project_config": {},
            "parsed_elements": [],
            "building_info": None,
            "classified_elements": {},
            "calculated_quantities": [],
            "material_list": [],
            "boq_data": None,
            "boq_file_paths": {},
            "validation_report": None,
            "warnings": [],
            "errors": [],
            "status": ProcessingStatus.PENDING,
            "current_step": "",
            "processing_log": [],
        }

        result = asyncio.run(agent.execute(state))
        assert result["status"] == ProcessingStatus.FAILED
        assert len(result["errors"]) > 0
